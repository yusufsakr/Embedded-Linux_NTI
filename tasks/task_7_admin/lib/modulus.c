/*
 * Name        : modulus
 * Arguments   : double x, double y
 * Return      : int
 * Description : Function recieves two numbers and returns the 
 *               remainder of deviding the first sent number by 
 *               the second one.
 */
int modulus (double x, double y) {
	return (int)x % (int)y;
}
